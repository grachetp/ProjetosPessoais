    private void btnDepositarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDepositarActionPerformed
        
        float valor = Integer.parseInt(txtValorDeposito.getText());
        this.contaLogada.depositar(valor);
        ContaDAO.salvaDeposito(this.contaLogada);
        JOptionPane.showMessageDialog(null,"Saldo Atual: " + this.contaLogada.getSaldo(), "Saque com sucesso!", 1);
        AtualizaDados();
        txtValorDeposito.setText(null);
        
    }//GEN-LAST:event_btnDepositarActionPerformed

    private void btnSacarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacarActionPerformed
        // TODO add your handling code here:
        float valor = Integer.parseInt(txtValorSacar.getText());
        boolean resp = this.contaLogada.sacar(valor);
        if(resp == false){
            JOptionPane.showMessageDialog(null, "Saldo Insuficiente!");
        }else{
            ContaDAO.salvaSaque(this.contaLogada);
            AtualizaDados();
        }
        txtValorSacar.setText(null);
    }//GEN-LAST:event_btnSacarActionPerformed

