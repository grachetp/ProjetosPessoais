    private void btnTranferirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTranferirActionPerformed
        // TODO add your handling code here:
        if(txtTranferenciaContaPrivilegiada.getText() == null || txtTranferenciaContaPrivilegiada.getText().trim().isEmpty() ||
                txtValorTranferir.getText() == null || txtValorTranferir.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos corretamente");
        }else{
            Conta contaPrivilegiada = new Conta();
            int num_contaprivilegiada = Integer.parseInt(txtTranferenciaContaPrivilegiada.getText());
            contaPrivilegiada = ContaDAO.buscarConta(num_contaprivilegiada);
            float valor = Integer.parseInt(txtValorTranferir.getText());
            if(contaPrivilegiada == null){
                JOptionPane.showMessageDialog(null, "Conta Privilegiada não encontrada!");
                txtTranferenciaContaPrivilegiada.setText(null);
            }else{
                boolean tranferencia = this.contaLogada.transferir(contaPrivilegiada, valor);
                if(tranferencia == true){
                    ContaDAO.salvaDeposito(contaPrivilegiada);
                    ContaDAO.salvaSaque(this.contaLogada);
                    JOptionPane.showMessageDialog(null, "Tranferido com sucesso!");
                }else{
                    JOptionPane.showMessageDialog(null, "Saldo Indisponivel!");
                }
            }
        }
        AtualizaDados();
    }//GEN-LAST:event_btnTranferirActionPerformed

